# 화면 출력
# 키워드 sep_seperator / 또는 - 로 구분하는 형태로 출력
year = 2021
month = 11
day = 15
print(year, month, day, sep="/")

hp1 = "010"    # 첫 글자의 숫자 0은 무시되어 따옴표로 묶어 표기한다. 
hp2 = "1234"
hp3 = "5678"
print(hp1, hp2, hp3, sep="-")

price = 1000
print(price, "원")
print(price, "원", sep="")

# NULL_값이 없는 것을 의미하여 쌍따옴표를 공백없이 붙여 표기, 0과 " "과는 다르다.

