# 문자열
# 문자열은 ''과 ""로 표기한다.
s='안녕하세요. 반갑습니다.'
print(s)

# 배열[]
# (소괄호) 함수 
# [대괄호] 배열/리스트 : 자료들이 연속해서 저장된 것
# {중괄호} 블럭 : 반복의 시작과 끝
# <다이아몬드 괄호> 제네릭 : 배열의 분류/서술/성질
# `` 팹틱 : 
print( s[2] )
print( s[8] )
print( s[0],s[1] )
print( s[0] + s[1] )   # +를 사용하여 문자를 붙여 출력한다.
print( s[ 0 : 2 ])     # 연속 : 시작 : 마지막-1 위치까지 표기
print( s[ 7 : 10 ])

